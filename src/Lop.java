
public class Lop {
	

	public static void main(String[] args) {
		int i=1;
		int num=30;
		while(i<=num) {
			if(i%5==0) {
				System.out.println(i);
				
				
			}
			i++;
			
		}
		i=num;
		System.out.println("Reverse order");
	while(i>=1) {
		if(i%5==0) {
		System.out.println(i);
	}
		i--;
		

	}

	}
}
